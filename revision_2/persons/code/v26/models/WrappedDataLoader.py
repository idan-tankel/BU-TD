class WrappedDataLoader:
    def __init__(self, dl, func):
        self.dl = dl
        self.func = func

    def __len__(self):
        return len(self.dl)

    def __iter__(self):
        batches = iter(self.dl)
        for b in batches:
            if b is not None:  # if the batch is None do nothing (iterate over the next one)
                yield (self.func(b))
